package com.geo.gnss.dao;

public class FourPar {
	protected boolean use;
	protected double cx;
	protected double cy;
	protected double ca;
	protected double ck;
	protected double orgx;
	protected double orgy;
	
	public boolean isUse() {
		return use;
	}
	public void setUse(boolean use) {
		this.use = use;
	}
	public double getCx() {
		return cx;
	}
	public void setCx(double cx) {
		this.cx = cx;
	}
	public double getCy() {
		return cy;
	}
	public void setCy(double cy) {
		this.cy = cy;
	}
	public double getCa() {
		return ca;
	}
	public void setCa(double ca) {
		this.ca = ca;
	}
	public double getCk() {
		return ck;
	}
	public void setCk(double ck) {
		this.ck = ck;
	}
	public double getOrgx() {
		return orgx;
	}
	public void setOrgx(double orgx) {
		this.orgx = orgx;
	}
	public double getOrgy() {
		return orgy;
	}
	public void setOrgy(double orgy) {
		this.orgy = orgy;
	}
	
	
}
